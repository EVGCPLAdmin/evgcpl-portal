/* ════════════════════════════════════════════════════════════════
   Step 1 · Project Setup
   ────────────────────────────────────────────────────────────────
   Changes from spec:
   • UUID          — hidden, auto-generated by backend
   • Company       — dropdown from 1-BillingMaster (auto-fills PAN/TAN)
   • Series        — readonly, auto-calculated Max+1 on new project
   • Active/Inactive → ACTIVE / INACTIVE (caps)
   • Planning In-Charge → dropdown from Employee Register (Employee_Ref)
   • Site In-Charge, Reporting Manager → same (Employee_Ref)
   • GST field key → 'GST' (was 'Client GST')
   • Added fields  → Site Address Line 3, Address, Country, Contact 3,
                     Website, Location, Delivery Address,
                     Purchase Billing Under, Payroll Under,
                     Mess/Attendance/SC Attendance In-Charges,
                     UserEmail, SystemEmail, Timestamp (all hidden)
═══════════════════════════════════════════════════════════════ */

window.PAGE = (function() {

  let allProjects = [];
  let companies   = [];
  let sites       = [];
  let employees   = [];   // [{ref, name}]
  let gstRowIdx   = 0;

  // ── FIELD_MAP — sheet column → element id ─────────────────────
  // Keep in sync with sheet headers. Backend reads these keys.
  const FIELD_MAP = {
    // Identity
    'Project Code':           'f_projectCode',
    'Project Name':           'f_projectName',
    'Series':                 'f_series',
    'Private / Govt':         'f_privateGovt',
    'Domestic / Inte':        'f_domesticInt',
    'Awarded Date':           'f_awardedDate',
    'Contract Amount':        'f_contractAmount',
    'Currency':               'f_currency',
    'Start Date':             'f_startDate',
    'End Date':               'f_endDate',
    'Active/Inactive?':       'f_active',
    // Company & Billing
    'Company':                'f_company',
    'PAN':                    'f_pan',
    'TAN':                    'f_tan',
    'Purchase Billing Under': 'f_purchaseBilling',
    'Payroll Under':          'f_payrollUnder',
    // Site
    'Site Name':              'f_siteName',
    'Site Code':              'f_siteCode',
    'Site Address Line 1':    'f_addr1',
    'Site Address Line 2':    'f_addr2',
    'Site Address Line 3':    'f_addr3',
    'Address':                'f_address',
    'City':                   'f_city',
    'State':                  'f_state',
    'Country':                'f_country',
    'Pin Code':               'f_pin',
    'Email ID':               'f_email',
    'Contact 1':              'f_contact1',
    'Contact 2':              'f_contact2',
    'Contact 3':              'f_contact3',
    'WebSite':                'f_website',
    'Location':               'f_location',
    'Delivery Address':       'f_deliveryAddress',
    // Client
    'Client Name':            'f_clientName',
    'Work Order Number':      'f_woNumber',
    'WO Date':                'f_woDate',
    'GST':                    'f_clientGst',   // Q4: map Client GST → sheet col 'GST'
    // Team
    'Site In Charge Name':    'f_siteIc',
    'Reporting Manager Name': 'f_reportMgr',
    'Planning In-Charge':     'f_planIc',
    'Accounts In-Charge':     'f_accIc',
    'Mess In-Charge':         'f_messIc',
    'Attendance In-Charge':   'f_attendanceIc',
    'SC Attendance In-Charge':'f_scAttendanceIc',
    // Auto-captured (hidden)
    'UUID':                   'f_uuid',
    'UserEmail':              'f_userEmail',
    'SystemEmail':            'f_systemEmail',
    'Timestamp':              'f_timestamp',
  };

  // ── Load ──────────────────────────────────────────────────────
  async function load() {
    await Promise.all([
      loadProjects(),
      loadMasterCompanies(),
      loadMasterSites(),
      loadEmployees(),
    ]);

    // Search filter on project list
    const search = document.getElementById('plpSearch');
    if (search) search.addEventListener('input',
      Utils.debounce(e => filterList(e.target.value), 200));

    // Site name → auto-fill
    const siteInput = document.getElementById('f_siteName');
    if (siteInput) {
      siteInput.setAttribute('list', 'sitesDatalist');
      ensureDatalist();
      siteInput.addEventListener('change', onSiteSelected);
      siteInput.addEventListener('blur',   onSiteSelected);
    }

    addGSTRow();

    // Restore active project if any
    const ap = window.STATE.activeProject;
    if (ap && ap['Project Code']) populateForm(ap);
  }

  function ensureDatalist() {
    let dl = document.getElementById('sitesDatalist');
    if (!dl) {
      dl = document.createElement('datalist');
      dl.id = 'sitesDatalist';
      document.body.appendChild(dl);
    }
    dl.innerHTML = sites.map(s =>
      `<option value="${Utils.esc(s.name)}">${Utils.esc(s.code || '')}</option>`
    ).join('');
  }

  // ── Sheet / master loaders ─────────────────────────────────────
  async function loadProjects() {
    try {
      allProjects = await API.gviz(window.CONFIG.TABS.PROJECT);
      allProjects = (allProjects || []).filter(p => p['Project Code']);
      renderList(allProjects);
    } catch (e) {
      console.error('loadProjects', e);
      const c = document.getElementById('projListContainer');
      if (c) c.innerHTML = '<div class="plp-empty">Could not load projects.<br>Check sheet sharing.</div>';
    }
  }

  async function loadMasterCompanies() {
    const C = window.CONFIG;
    try {
      const data = await API.gviz(C.MASTER_TABS.BILLING, C.MASTER_SHEET_ID);
      companies = (data || [])
        .map(r => ({
          name: r['Billing Name'] || r['Company'] || r['Name'] || '',
          pan:  r['PAN'] || '',
          tan:  r['TAN'] || '',
        }))
        .filter(c => c.name);
    } catch (e) { companies = []; }

    const sel = document.getElementById('f_company');
    if (!sel) return;
    sel.innerHTML = '<option value="">— Select Company —</option>' +
      companies.map(c => `<option value="${Utils.esc(c.name)}">${Utils.esc(c.name)}</option>`).join('');

    sel.addEventListener('change', () => {
      const c = companies.find(x => x.name === sel.value);
      if (!c) return;
      if (c.pan) { const el = document.getElementById('f_pan'); if (el && !el.value) el.value = c.pan; }
      if (c.tan) { const el = document.getElementById('f_tan'); if (el && !el.value) el.value = c.tan; }
    });
  }

  async function loadMasterSites() {
    const C = window.CONFIG;
    try {
      const data = await API.gviz(C.MASTER_TABS.SITE, C.MASTER_SHEET_ID);
      sites = (data || [])
        .map(r => ({
          name:           r['Site Name']            || r['SiteName']            || r['Name'] || '',
          code:           r['Site ID']              || r['SiteID']              || r['Code'] || r['Series'] || '',
          pan:            r['PAN']                  || '',
          tan:            r['TAN']                  || '',
          gst:            r['GST']                  || '',
          addr1:          r['Site Address Line 1']  || r['Address Line 1'] || r['Addr1'] || '',
          addr2:          r['Site Address Line 2']  || r['Address Line 2'] || r['Addr2'] || '',
          addr3:          r['Site Address Line 3']  || r['Address Line 3'] || r['Addr3'] || '',
          address:        r['Address']              || '',
          city:           r['City']                 || '',
          state:          r['State']                || '',
          country:        r['Country']              || '',
          pin:            r['Pin Code']             || r['PIN Code'] || r['PIN'] || '',
          email:          r['Email ID']             || r['Email']   || '',
          contact1:       r['Contact 1']            || r['Contact1'] || '',
          contact2:       r['Contact 2']            || r['Contact2'] || '',
          contact3:       r['Contact 3']            || r['Contact3'] || '',
          website:        r['WebSite']              || r['Website'] || '',
          location:       r['Location']             || '',
          deliveryAddr:   r['Delivery Address']     || '',
          purchaseBilling:r['Purchase Billing Under']|| '',
          payrollUnder:   r['Payroll Under']        || '',
          siteIc:         r['Site In Charge Name']  || r['Site In-Charge'] || '',
          reportMgr:      r['Reporting Manager Name']|| r['Reporting Manager'] || '',
          planIc:         r['Planning In-Charge']   || '',
          accIc:          r['Accounts In-Charge']   || '',
          messIc:         r['Mess In-Charge']       || '',
          attendanceIc:   r['Attendance In-Charge'] || '',
          scAttendanceIc: r['SC Attendance In-Charge'] || '',
          status:         r['Active/Inactive?']     || r['Status'] || 'ACTIVE',
        }))
        .filter(s => s.name && !/inactive/i.test(String(s.status || '')));
      ensureDatalist();
    } catch (e) {
      console.warn('loadMasterSites', e.message);
      sites = [];
    }
  }

  async function loadEmployees() {
    const C = window.CONFIG;
    try {
      const data = await API.gviz(C.EMPLOYEE_TAB, C.EMPLOYEE_SHEET_ID);
      employees = (data || [])
        .map(r => ({
          ref:  r['Employee_Ref'] || r['Emp_Ref'] || r['Emp Code'] || r['EmpCode'] || '',
          name: r['Name']         || r['Employee Name'] || r['Full Name'] || '',
        }))
        .filter(e => e.ref || e.name);
    } catch (e) { employees = []; }

    // Populate all three employee dropdowns
    ['f_siteIc', 'f_reportMgr', 'f_planIc'].forEach(id => {
      const sel = document.getElementById(id);
      if (!sel) return;
      sel.innerHTML = '<option value="">— Select Employee —</option>' +
        employees.map(e => {
          const label = e.ref && e.name ? `${e.ref} · ${e.name}` : (e.ref || e.name);
          const val   = e.ref || e.name;
          return `<option value="${Utils.esc(val)}">${Utils.esc(label)}</option>`;
        }).join('');
    });
  }

  // ── Site auto-fill ─────────────────────────────────────────────
  function onSiteSelected() {
    const inp = document.getElementById('f_siteName');
    if (!inp) return;
    const value = (inp.value || '').trim();
    if (!value) return;
    const s = sites.find(x => x.name.toLowerCase() === value.toLowerCase());
    if (!s) return;

    setIfEmpty('f_siteCode',        s.code);
    setIfEmpty('f_addr1',           s.addr1);
    setIfEmpty('f_addr2',           s.addr2);
    setIfEmpty('f_addr3',           s.addr3);
    setIfEmpty('f_address',         s.address);
    setIfEmpty('f_city',            s.city);
    setIfEmpty('f_state',           s.state);
    setIfEmpty('f_country',         s.country);
    setIfEmpty('f_pin',             s.pin);
    setIfEmpty('f_email',           s.email);
    setIfEmpty('f_contact1',        s.contact1);
    setIfEmpty('f_contact2',        s.contact2);
    setIfEmpty('f_contact3',        s.contact3);
    setIfEmpty('f_website',         s.website);
    setIfEmpty('f_location',        s.location);
    setIfEmpty('f_deliveryAddress', s.deliveryAddr);
    setIfEmpty('f_purchaseBilling', s.purchaseBilling);
    setIfEmpty('f_payrollUnder',    s.payrollUnder);
    setIfEmpty('f_pan',             s.pan);
    setIfEmpty('f_tan',             s.tan);
    setIfEmpty('f_accIc',           s.accIc);
    setIfEmpty('f_messIc',          s.messIc);
    setIfEmpty('f_attendanceIc',    s.attendanceIc);
    setIfEmpty('f_scAttendanceIc',  s.scAttendanceIc);
    if (s.siteIc)   setSelectIfEmpty('f_siteIc',   s.siteIc);
    if (s.reportMgr)setSelectIfEmpty('f_reportMgr', s.reportMgr);
    if (s.planIc)   setSelectIfEmpty('f_planIc',    s.planIc);

    Utils.toast(`Loaded site details: ${s.code || s.name}`, 'ok');
    inp.value = s.name;
  }

  function setIfEmpty(id, val) {
    const el = document.getElementById(id);
    if (!el || el.value || !val) return;
    el.value = val;
  }
  function setSelectIfEmpty(id, val) {
    const el = document.getElementById(id);
    if (!el || el.value || !val) return;
    const exists = Array.from(el.options).some(o => o.value === val || o.textContent.includes(val));
    if (!exists) {
      const opt = document.createElement('option');
      opt.value = val; opt.textContent = val;
      el.appendChild(opt);
    }
    // Try exact match first, then partial
    const exact = Array.from(el.options).find(o => o.value === val);
    el.value = exact ? exact.value : val;
  }

  // ── Project list ───────────────────────────────────────────────
  function renderList(list) {
    const c = document.getElementById('projListContainer');
    if (!c) return;
    if (!list.length) {
      c.innerHTML = '<div class="plp-empty">No projects yet.<br>Click + New Project to add one.</div>';
      return;
    }
    const ap = window.STATE.activeProject;
    const apCode = ap ? ap['Project Code'] : null;
    c.innerHTML = list.map(p => {
      const code = Utils.esc(p['Project Code']);
      const name = Utils.esc(p['Project Name'] || '(no name)');
      const isActive = code === apCode;
      return `<div class="plp-row ${isActive ? 'active' : ''}" onclick="PAGE.selectProject('${code}')">
        <span class="plp-code">${code}</span>
        <span class="plp-name">${name}</span>
      </div>`;
    }).join('');
  }
  function filterList(q) {
    const ql = String(q || '').toLowerCase();
    renderList(allProjects.filter(p =>
      String(p['Project Code'] || '').toLowerCase().includes(ql) ||
      String(p['Project Name'] || '').toLowerCase().includes(ql)
    ));
  }
  function selectProject(code) {
    const p = allProjects.find(x => x['Project Code'] === code);
    if (!p) return;
    window.STATE.activeProject = p;
    window.STATE.months = Utils.genMonths(p['Start Date'], p['End Date']);
    if (window.persistState) window.persistState();
    populateForm(p);
    renderList(allProjects);
    Utils.toast(`Loaded: ${p['Project Code']}`, 'ok');
  }

  // ── Form population ────────────────────────────────────────────
  function populateForm(p) {
    Object.keys(FIELD_MAP).forEach(col => {
      const el = document.getElementById(FIELD_MAP[col]);
      if (!el) return;
      let v = p[col] != null ? p[col] : '';

      // Date normalisation
      if (el.type === 'date') { v = toDateInput(v); }

      el.value = v;
    });

    // GST multi-value
    document.getElementById('gstBillingList').innerHTML = '';
    gstRowIdx = 0;
    const gstField = p['GST'] || p['Client GST'] || p['GST Billing IDs'] || '';
    if (gstField) {
      String(gstField).split(/[|,;]/).forEach(g => { const t = g.trim(); if (t) addGSTRow(t); });
    }
    if (!document.getElementById('gstBillingList').children.length) addGSTRow();
  }

  function toDateInput(v) {
    if (!v) return '';
    const d = new Date(v);
    if (!isNaN(d)) return d.toISOString().slice(0, 10);
    return '';
  }

  // ── New project ────────────────────────────────────────────────
  function newProject() {
    window.STATE.activeProject = null;
    if (window.persistState) window.persistState();

    // Clear all mapped fields
    Object.values(FIELD_MAP).forEach(id => {
      const el = document.getElementById(id);
      if (el) el.value = '';
    });
    document.getElementById('gstBillingList').innerHTML = '';
    addGSTRow();

    // Defaults
    document.getElementById('f_active').value   = 'ACTIVE';
    document.getElementById('f_currency').value = 'INR';

    // Auto-calculate Series = max + 1
    const maxSeries = allProjects.reduce((mx, p) => {
      const s = Number(p['Series'] || 0);
      return s > mx ? s : mx;
    }, 0);
    document.getElementById('f_series').value = String(maxSeries + 1);

    // Auto-capture login email for UserEmail + SystemEmail
    _stampAutoFields();

    renderList(allProjects);
    document.getElementById('f_projectName').focus();
    Utils.toast('New project ready — fill in and Save', 'ok');
  }

  // Stamp hidden auto-fields with current values
  function _stampAutoFields() {
    const email = (window.STATE.user && (window.STATE.user.email || window.STATE.user.Email)) || '';
    const ueEl  = document.getElementById('f_userEmail');
    const seEl  = document.getElementById('f_systemEmail');
    if (ueEl && !ueEl.value) ueEl.value = email;
    if (seEl && !seEl.value) seEl.value = email;
    // Timestamp will be set by backend on save — pre-fill with now for reference
    const tsEl  = document.getElementById('f_timestamp');
    if (tsEl && !tsEl.value) {
      const now = new Date();
      tsEl.value = now.toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' })
                 + ' ' + now.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit', hour12: false });
    }
  }

  // ── GST rows ───────────────────────────────────────────────────
  function addGSTRow(val) {
    const v = val || '';
    const idx = gstRowIdx++;
    const wrap = document.getElementById('gstBillingList');
    const div = document.createElement('div');
    div.className = 'multi-value-row';
    div.dataset.idx = idx;
    div.innerHTML = `
      <span class="tag-label">GST ${idx + 1}</span>
      <input type="text" class="gst-input" value="${Utils.esc(v)}"
        placeholder="22AAAAA0000A1Z5" maxlength="15" style="text-transform:uppercase" />
      <button class="btn-icon danger" onclick="PAGE.removeGSTRow(this)" title="Remove">&times;</button>
    `;
    wrap.appendChild(div);
  }
  function removeGSTRow(btn) {
    const row = btn.closest('.multi-value-row');
    if (row) row.remove();
    if (!document.getElementById('gstBillingList').children.length) addGSTRow();
  }

  // ── Collect form data ──────────────────────────────────────────
  function collectForm() {
    const data = {};
    Object.keys(FIELD_MAP).forEach(col => {
      const el = document.getElementById(FIELD_MAP[col]);
      data[col] = el ? el.value : '';
    });

    // GST multi-value → pipe-separated
    const gsts = Array.from(document.querySelectorAll('.gst-input'))
      .map(i => i.value.trim()).filter(Boolean);
    data['GST'] = gsts.join('|');

    // Always stamp login email at collect time (in case page was pre-loaded)
    const email = (window.STATE.user && (window.STATE.user.email || window.STATE.user.Email)) || '';
    data['UserEmail']   = email;
    data['SystemEmail'] = email;

    // Timestamp — backend will reformat, but send current
    const now = new Date();
    data['Timestamp'] = now.toLocaleDateString('en-GB', {
      day: 'numeric', month: 'long', year: 'numeric'
    }) + ' ' + now.toLocaleTimeString('en-GB', {
      hour: '2-digit', minute: '2-digit', hour12: false
    });

    return data;
  }

  // ── Save ───────────────────────────────────────────────────────
  async function save() {
    const data = collectForm();
    if (!data['Project Name']) {
      Utils.toast('Project Name is required', 'err'); return;
    }
    const btn = document.getElementById('saveBtn');
    if (btn) { btn.disabled = true; btn.textContent = 'Saving…'; }
    try {
      const r = await API.scriptCall('saveProjectSetup', data);
      if (r && r.success) {
        Utils.toast('Project saved ✓', 'ok');
        if (r.projectCode) document.getElementById('f_projectCode').value = r.projectCode;
        if (r.uuid)        document.getElementById('f_uuid').value        = r.uuid;
        if (r.series)      document.getElementById('f_series').value      = r.series;
        await loadProjects();
      } else {
        Utils.toast((r && r.message) || 'Save failed', 'err');
      }
    } catch (e) {
      Utils.toast('Save error: ' + e.message, 'err');
    } finally {
      if (btn) { btn.disabled = false; btn.textContent = '💾 Save Project'; }
    }
  }

  async function refreshList() {
    await loadProjects();
    Utils.toast(`Loaded ${allProjects.length} projects`, 'ok');
  }

  function onProjectChange() {
    const ap = window.STATE.activeProject;
    if (ap) populateForm(ap);
    renderList(allProjects);
  }

  return {
    load, save, refreshList, onProjectChange,
    selectProject, newProject,
    addGSTRow, removeGSTRow,
  };
})();
